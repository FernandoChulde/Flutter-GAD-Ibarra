class TipoFicha {
  String fit_id;
  String fit_nombre;
  String fit_descripcion;
  String fit_departamento;

  TipoFicha(fit_id, fit_nombre, fit_descripcion, fit_departamento) {
    this.fit_id = fit_id;
    this.fit_nombre = fit_nombre;
    this.fit_descripcion = fit_descripcion;
    this.fit_departamento = fit_departamento;
  }
}
